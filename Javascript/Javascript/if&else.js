// basicamente se tiver algo terá algo
// se enão tiver esse algo terá outro algo
   /*
    Controlador de fluxo
    - IF (SE)
    - ELSE (SE NÂO)

    operadores de comparação:
       maior que >
       menor que <
       igual que ==
*/

const notaDoaluno = 3
const notaDecorte = 5

if(notaDoaluno > notaDecorte){
// o que será executado se o IF for verdadeiro e se for falsa ele ignorará
  
   console.log("parabéns,você passou de ano")

} else {
//    com o else se o if for falso ele entra aqui
     
    console.log("você é burro")

}
// ele nunca vai aparecer nos dois ao mesmo tempo

